/***** ImageObject : 画像表示 *****/
class ImageObject
{
private:

	/***** m_image: 画像 *****/
	TileTexture m_image;

	/***** m_alias: エイリアス *****/
	char *m_alias;

	/***** m_normal: 法線ベクトル *****/
	float m_normal[3];

	/***** m_face: 片面表示 *****/
	bool m_face;

	/***** m_enable: 有効 *****/
	bool m_enable;

	/***** initialize: 初期化 *****/
	void initialize();

	/***** clear: クリア *****/
	void clear();

public:

	/***** TextObject: コンストラクタ *****/
	ImageObject();

	/***** ~TextObject: デストラクタ *****/
	~ImageObject();

	/***** isEnable: 画像の有効確認 *****/
	bool isEnable();

	/***** set: 画像のセット *****/
	bool set(const char *alias, const char *fileName, float *size, float *position, float *rotation, bool face);

	/***** change: 画像の再セットセット *****/
	bool change(const char *fileName);

	/***** release: 画像の開放 *****/
	void release();

	/***** render: 画像のレンダリング *****/
	void render();

	/***** getAlias: エイリアスの取得 *****/
	char *getAlias();
};
